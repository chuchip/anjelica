    public void setNumFax(String numfax)
    {
        if (! numfax.equals(""))
        {
          numfax = numfax.replaceAll("/", "");
          numfax = numfax.replaceAll("\\.", "");
          numfax = numfax.replaceAll("-", "");
        }
        cli_faxE.setText(numfax);
        respuestE.requestFocus();
    }
